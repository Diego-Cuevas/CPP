//This program calculate the area of a square whose sides measure 5m
#include <iostream>
using namespace std;

int main(int argc, char *argv[]) {
    
    int side = 5, area = 0;

    area = side * side;

    cout << "The area of the square is: " << area << "m" << endl;

    return 0;
}

