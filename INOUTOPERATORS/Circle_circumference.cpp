//Program to calculate the circumference of a circle
#include <iostream>
using namespace std;

int main(int argc, char *argv[]) {
	
	int ratio = 15;
	float pi = 3.1416, circumference = 0;
	
	circumference = 2 * pi * ratio;
	
	cout << "The size of circumference is " << circumference << endl;
	
	return 0;
}

